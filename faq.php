<h1>Frequently Asked Questions</h1>

<strong>Q.  Why Shank Hall?  (Why the Stonehenge?)</strong><br>
A.  The name Shank Hall comes from the musicians' cult film <a href="http://www.spinal.tap.com">This Is Spinal Tap</a>.  In the film, Spinal Tap played in Milwaukee at a then fictitious club named Shank Hall.  The club's owner, Peter Jest, booked the Tap at the UWM Ballroom July 11, 1984.  He then promised the band if he ever opened a club in Milwaukee it would be named Shank Hall.  Guess what?  He did.  In 1989.  You can read more about the legend of Shank Hall in the Spinal Tap Shrine during your next visit to the club.<br>
<br>

<strong>Q.  Has Spinal Tap ever played at Shank Hall?</strong><br>
A.  No.  The band only played a show at the Ballroom.  The Tap did, however, hold a press conference here in 1992.<br>
<br>

<strong>Q.  Didn't Shank Hall burn down in the early '90s?</strong><br>
A.  Yes.  While Shank Hall didn't burn to the ground, the club was damaged by fire in 1992.  Hard work and determination has allowed Shank Hall to be rebuilt into the premier club it is today.  Incidentally, the fire was started by a smoldering cigarette - use those ash trays kids.  (You can read more about the great fire of Shank Hall while seeing you favorite act.  All the press clippings are posted.)<br>
<br>

<strong>Q.  What was Shank Hall before Shank Hall?</strong><br>
A.  It was another club, albeit a much different one: Teddy's.<br>
<br>

<strong>Q.  When did Shank Hall remodel?</strong><br>
A.  During the summer of 1999.<br>
<br>

<strong>Q.  What's Shank Hall's capacity?</strong><br>
A.  A mere 300 people.  Get there early.<br>
<br>

<strong>Q.  How early should I get there?</strong><br>
A.  Doors don't open until one hour before show time.  If you show up earlier than that, you'll be waiting outside.<br>
<br>

<strong>Q.  What's with all the photos on the walls?</strong><br>
A.  Each artist with their photo on the walls of Shank have played the club, at one time or another.<br>
<br>

<strong>Q.  Where can I park?</strong><br>
A.  There is plenty of free street parking in the area.  Please do not use any nearby parking lots and <strong>please do not block the driveway.  Ever</strong>.<br>
<br>

<strong>Q.  Can I get advance tickets to shows at Shank Hall?</strong><br>
A.  Yes, for many of our shows.  Full information can be found in our <a href="tickets.php">Ticket</a> section.<br>
<br>

<strong>Q.  When is the box office open?</strong><br>
A.  Shank Hall's box office is open Tuesdays from 11AM to 2PM, as well as on each show date, from a few hours before show time through the end of the show.  Call 414-276-7288 for daily updates.<br>
<br>

<strong>Q.  Can I come get a beer at Shank Hall during Packer games?  (or anytime for that matter?)</strong><br>
A.  No.  Shank Hall is a music venue, and therefore open only during performances.  Doors, as well as the bar, always open an hour before show time.<br>
<br>

<strong>Q.  Does Shank Hall serve food?</strong><br>
A.  We have a wide variety of chips and pretzels available.<br>
<br>

<strong>Q.  Can I bring in carry-ins?</strong><br>
A.  No outside beverages are allowed inside Shank Hall, but you can bring in food, or even order a pizza.  (We don't want you drinking on an empty stomach.)<br>
<br>

<strong>Q.  Do you allow audio/video recordings?</strong><br>
A.  The general rule is no.  No audio taping, no video taping, no recording of the event without the artist's permission.  But then it depends on the artist - many local acts don't seem to mind photos without flash.  Find out from the artist, call ahead, or bring your camera down - we may just tell you to return it to your car.<br>
<br>

<strong>Q.  What can I do with my coat?</strong><br>
A.  We have a coat check for you - $2.00 please.<br>
<br>

<strong>Q.  Why are there No Passouts?</strong><br>
A.  It's to help continue our good relationship with our residential neighbors.  All it takes is one unhappy neighbor and there will be no more shows at Shank Hall.<br>
<br>

<strong>Q.  Why are shows 21+?</strong><br>
A.  Shows at Shank Hall are 21 and over because it's the law.  No one will be admitted without proper ID.  The only exception we will make is if you are accompanied by a parent or legal guardian during the show (they can't just drop you off).  AND you have to prove they really are your parent or legal guardian.<br>
<br>

